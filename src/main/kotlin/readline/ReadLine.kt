package readline

/**
 * Lendo dados com um readLine
Se você estiver trabalhando com versões mais antigas da linguagem Kotlin,
precisará usar readLine()!!em vez de readln(). Essas funções fazem a mesma coisa,
mas readln()são mais curtas e preferidas, portanto, se você estiver
trabalhando com Kotlin 1.6 ou superior, use readln().
fonte: https://hyperskill.org/
 **/

fun main() {
    val line = readln()
    println(line)
}
