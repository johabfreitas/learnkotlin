package operadoresaritmeticos

/**
 *
 */

fun main() {
    // Adição
    println(13 + 25) // 38
    println(20 + 70) // 90

    // Subtração
    println(70 - 30) // 40
    println(30 - 70) // -40

    // Multiplicação
    println(21 * 3) // 63
    println(20 * 10) // 200

    // Divisão. O / operador(barra) divide as partes inteiras de dois números; a parte fracionária é descartada.
    println(8 / 3) // 2
    println(41 / 5) // 8

    // Resto da divisão. O % operador(porcentagem) encontra o resto de uma divisão:
    println(10 % 3) // 1
    println(12 % 4) // 0
}